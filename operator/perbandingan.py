#operator perbandingan di gunakan untuk membandingan suatu nilai di python


i = 10
j = 5

print(i == j) # operator == 'sama dengan'

print(i != j) # operator != 'tidak sama dengan'

print(i > j) # lebih besar dari '>'

print(i < j) # lebih kecil dari '<'

print(i >= j) # lebih besar dari atau sama dengan '<='

print(i <= j) # lebih kecil dari atau sama dengan '<='






