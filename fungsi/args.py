# args atau bisa juga disebut arguments
# args terkadang juga disebut sebagai magic arguments

def nama(*orang):
    for nama in orang:
        print(f'nama : {nama}')

nama('goku', 'luffy', 'naruto') # hanya dengan menambahkan * pada parameter fungsi kita bisa mengirim banyak argumen ke fungsi










